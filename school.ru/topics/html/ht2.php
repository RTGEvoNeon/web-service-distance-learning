<p> <div align="left" align="justify">&nbsp;&nbsp;&nbsp;&nbsp;Веб-браузеры автоматически переносят текст внутри абзацев на новую строку. Если стоит задача размещения какого-либо сочетания слов в одной строке, то для этой цели используется тэг (тег) <font color="#008000">&lt;NOBR></font>.
<p>Для перехода к новой строке используется тэг (тег) <font color="#008000">&lt;BR></font>. Он сообщает браузеру, что следует прекратить размещение текста и других элементов страницы в пределах текущей строки и перейти к новой строке. Этот тэг (тег) не имеет своего двойника с косой чертой, так называемого закрывающего тэга (тег).<br>
&nbsp;&nbsp;&nbsp;&nbsp;Основное использование этого тэга (тега) - для принудительного размещения встроенных элементов в определенном месте страницы. Он удобен, также, в тех случаях, когда необходимо увеличить пустые области между отдельными элементами страницы.
