<p> <div align="left" align="justify">&nbsp;&nbsp;&nbsp;&nbsp;Веб-браузеры обычно игнорируют дополнительные символы пробела, введенные в текст страницы. Для этого используется команда <font color="#008000">&amp;nbsp</font>. Эта команда создает неделимое пустое пространство между элементами страницы, будь то графика или текст. Следует учитывать тот факт, что использование <font color="#008000">&amp;nbsp</font> вместо пробела между двумя соседними словами гарантирует, что оба слова не будут разнесены по разным строкам в окне браузера - они будут расположены всегда рядом.</p>

