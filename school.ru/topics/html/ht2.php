<a name="1"></a><h5>Обязательные тэги (теги)</h5><br>
&nbsp;&nbsp;&nbsp;&nbsp;Существует несколько основных тэгов (тегов) HTML, которые должны присутствовать в тексте любой Вэб-страницы.
			Каждая Вэб-страница обязана содержать тэг (тег)<strong><font color="#008000">&lt;HTML></font></strong>, располагаемый в самом начале. Этот тэг (тег) описывает ваш документ как Вэб-страницу, выполненную в формате HTML. Непосредственно за дескриптором <strong><font color="#008000">&lt;HTML></font></strong> обычно следует тэг (тег) <strong><font color="#008000">&lt;HEAD></font></strong>, который указывает на наличие текста, содержащего наименование страницы и дополнительные сведения о ней.<br>

В раздел HEAD обычно вложен тэг (тег) <font color="#008000"><strong>&lt;TITLE></strong></font>, служащий для обозначения наименования страницы. Наименования страниц обычно отображаются в строке заголовка окна браузера. Название Веб-страницы может быть набрано только с помощью символов букв, цифр и пробела.</p>
<p>Затем следует тэг (тег) <font color="#008000"><strong>&lt;BODY></strong></font>, который указывает на начало собственно "тела" Веб-страницы. В этом разделе размещаются весь остальной текст, графика, таблицы и другие элементы содержимого страницы, которые увидит посетитель, обратившийся к вашему сайту.</p>
<p><font color="#FF00FF">Пример:</font>
<pre>
&lt;html>

&lt;head>
&lt;title>Online обучение: HTML&lt;/title>
&lt;/head>

&lt;body>
Текст сайта

&lt;/body>

&lt;/html>
</pre>
&nbsp;&nbsp;&nbsp;&nbsp; Нормальный HTML-редактор уже имеет набор вышеуказанных тэгов (тегов) при создании новой страницы.
Следует сказать, что к выбору наименований Вэб-страниц следует отнестись со всей серьезностью, т.к. они используются поисковыми системами для формирования каталогов Веб-страниц. Поисковая система - это сайт, помогающий пользователям быстро находить интересующую их информацию. Ваша страница с большей вероятностью окажется найденной поисковой системой, которой задан определенный набор ключевых слов поиска, если эти слова присутствуют в названии страницы. Среди самых популярных поисковых систем следует назвать Yandex, Google, Aport, Yahoo, Rambler.
<br>
<a name="2"></a><h5>Создание абзаца, символов пробела, заголовка</h5><br>
Веб-браузеры обычно не принимают во внимание те символы перевода строки, которые вносятся в текст пользователем на этапе ее формирования. Для начала нового абзаца служит тэг (тег) <font color="#008000"><strong>&lt;P></strong></font>.
После открытия страницы в окне Веб-браузера все абзацы ее текста, помеченные тэгом (тегом) <font color="#008000"><strong>&lt;P></strong></font>, разделяются пустыми строками, что улучшает ее компоновку и внешний вид.
По умолчанию браузер обычно форматирует абзац с выравниванием по левому полю. Для принудительного выравнивания служит атрибут <font color="#008000">align</font>. С его помощью абзацы могут быть выровнены влево, вправо, по центру и по ширине.
