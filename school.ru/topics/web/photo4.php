<strong><h2>Лекция № 4</h2> </strong><br><br><hr><br><br>


<p> <div align="left" align="justify">&nbsp;&nbsp;&nbsp;&nbsp;

<strong>Формы и фигуры - это области или массы, которые определяют объекты в пространстве. </strong></span><span style="color:#000000;">Форма и фигура подразумевают пространство; естественно, ведь без него они не могут существовать.</span><br />
&nbsp;&nbsp;</div>



<h3 style="text-align: center"><span style="color:#993300;">Форма, фигура и пространство</span></h3>
<div style="text-align: justify">Существует два способа типизации форм и фигур. Формы и фигуры могут быть двухмерными, либо трехмерными.. двухмерная форма имеет ширину и длину. Она также может служить иллюзорным изображением трехмерных объектов. Трехмерные фигуры имеют также высоту и толщину.<br />
&nbsp;<br />
<strong>Формы и фигуры также бывают органическими и геометрическими.</strong> Органические формы, такие например, как холмы, покрытые снегом имеют неправильную форму и часто асимметричны. Органические формы чаще всего имеют природное происхождение.<br />

<br />
&nbsp;</div>
<div style="text-align: center">&nbsp;<img align="absMiddle" alt="" style="null" src="image/form2.png" />&nbsp;</div>
<div style="text-align: justify"><br />
<br />
&nbsp;</div>
<div style="text-align: justify"><strong>Геометрические формы</strong> - это такие формы, которые соответствуют известным геометрическим фигурам, например, квадратам, прямоугольникам, кругам, кубам, сферам, конусам, и прочим правильным фигурам. Эти формы чаще всего имеют искусственное происхождение. <br />
&nbsp;<br />
Однако не все предметы, сделанные руками человека, имеют правильную геометрическую форму; многие такие предметы имеют неправильные контуры. Хотя данное кимоно по конструкции является геометрической формой, его поверхность имеет органическую, неправильную форму.<br />
<br />

&nbsp;</div>
<div style="text-align: center"><img align="absMiddle" alt="" style="null" src="image/form3.png" /></div>
<div style="text-align: justify"><br />
<br />
Также и природные объекты тоже не всегда имеют неправильные формы; снежинки или мыльные пузыри - пример того, как природные объекты могут быть правильными геометрическими фигурами. <br />
<br />
&nbsp;</div>
<div style="text-align: center"><img alt="" align="absMiddle" src="image/ip2.png" /></div>
<div style="text-align: justify"><br />
Существуют также и другие термины, с помощью которых описываются формы и фигуры в композициях; эти классификации связаны с тем, как формы представлены. Если мы в форме или фигуре можем распознать известный нам объект или обстановку, мы называем ее реалистичной или натуралистичной. Однако, если в изображении трудно или невозможно обнаружить объекты, знакомые нам по опыту, то мы называем такие изображения абстрактными.<br />
&nbsp;<br />
Существует несколько видов абстрактных изображений. По большей части, абстракции берут свое начало от реалистических изображений, которые подвергаются каким-то искажениям, причем таким, что исходный объект не так то просто распознать. Пример такой абстракции - рисунки цветков Джоржиа О'Кифа.&nbsp;<br />

&nbsp;<br />
Этот вид абстракции в искусстве называют объективным изображением - то есть изображением, которое произошло от реального объекта. С другой стороны, некоторые произведения абстрактного искусства базируются на чистой комбинации форм, линий и цвета, и не соотносятся ни с каким реально существующим объектом или обстановкой. Такие изображения называют необъективными.<br />
&nbsp;<br />
Карикатуры являются особым видом абстракции, в которой реалистическое изображение искажается для того, чтобы охарактеризовать человека, место или другой объект. С этим методом абстракции мы пожалуй знакомы лучше всего, так как он постоянно присутствует в различных средствах массовой информации.&nbsp;<br />
&nbsp;<br />
Тем не менее, важно помнить, что если бы более до этого более сильные методы абстракции не пробили бы себе дорогу экспериментами с искажениями изображений, мы бы сейчас не имели бы карикатуры как таковой. Столетие назад, карикатур (таких какие мы имеем сейчас) не было вообще.<br />
&nbsp;<br />
Наше восприятие фигур и форм зависит от множества факторов. Расположение точки зрения, с которой мы видим объект, может подчеркнуть или скрыть некоторые его свойства, и следовательно снизить или повысить эффект, который он производит на смотрящего. Как видно на серии данных фотографий, где изображен один и тот же манекен, пространство вокруг предмета может отвлекать, привлекать внимание или изменять вид предмета. Хаотический фон за предметом уменьшает важность самого предмета, в то время как чистый, однородный фон - привлекает к предмету внимание.<br />
<br />
&nbsp;</div>
