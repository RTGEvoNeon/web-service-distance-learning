<h3 style="text-align: center"><span style="color:#993300;">Связь формы с положительным или отрицательным пространством</span></h3>
<div style="text-align: justify">Формы и фигуры можно классифицировать как положительные и отрицательные. В двухмерной композиции объекты являются положительными формами, а фон - отрицательным пространством. Для начинающих художников и дизайнеров очень важным является умение эффективно использовать отрицательное пространство<br />
<br />
&nbsp;</div>
<div style="text-align: center"><img align="absMiddle" alt="" style="null" src="image/form16.png" /></div>
<div style="text-align: justify">&nbsp;<br />
&nbsp;Данный пример (см. рис.) - результат упражнения, когда студенту дается задание вырезать из бумаги определенную композицию используя белую и черную бумагу. Это позволяет избавиться от привычки игнорировать фон и рассматривать его просто как пустое пространство. Эффективное расположение объектов в отношении к отрицательному пространству - очень важно для получения успешной композиции.<br />
&nbsp;<br />
Некоторые художники &quot;играя&quot; с обращением отрицательного пространства в положительное и обратно, создают сложные иллюзии. Работы М.К.Эшера (M. C. Escher ) играют на нашем ощущении того, что является фоном, а что - первым планом. Другие художники используют эту иллюзию положительных/отрицательных изображений в еще большей мере, пряча одно изображение внутри другого.&nbsp;<br />

&nbsp;<br />
Восприятие формы и фигур определяется нашим врожденным инстинктом, который приписывает определенные значения и порядок определенным визуальным образам. Когда мы смотрим на изображение и формируем первое к нему отношение, как правило именно на этом этапе мы и приходим к заключению о том, что изображено на картине, и игнорируем все остальные возможные варианты. Из-за этого нам трудно увидеть другие скрытые изображения.<br />
&nbsp;<br />
Тренируясь воспринимать изображение более глубоко, вы сделаете первый большой шаг к развитию своей настоящей визуальной грамотности.<br />


