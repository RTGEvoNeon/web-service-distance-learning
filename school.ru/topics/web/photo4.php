<h3 style="text-align: center"><span style="color:#993300;">Двухмерная форма</span></h3>
<div style="text-align: justify"><strong>Двухмерные формы</strong> - основа организации рисунка или композиции в изобразительном искусстве, фотографии и проч. Форма создается несколькими способами.<br />
&nbsp;<br />
Она может быть определена линией. Об этом было сказано выше. Линия, либо нарисованная явно, либо только угадываемая, создает контуры формы.<br />
<br />
<br />
&nbsp;</div>
<div style="text-align: center"><img align="absMiddle" alt="" style="null" src="image/form11.png" />&nbsp; &nbsp;<img alt="" align="absMiddle" src="image/form12.png" /></div>
<div style="text-align: justify"><br />
<strong>Яркость</strong> (относительная яркость или затемненность цвета) также может быть использована для создания формы.<br />

&nbsp;<br />
Сильный контраст по яркости в композиции может на границах создавать иллюзию контура. Градации яркости, или полутени, также могут использоваться для создания иллюзии формы или объема. Варицаия темных и белых участков может придавать форме трехмерность.<br />
<br />
&nbsp;</div>
<div style="text-align: center"><img align="absMiddle" alt="" style="null" src="image/form13.png" />&nbsp; &nbsp;<img alt="" align="absMiddle" src="image/form14.png" /></div>
<div style="text-align: justify"><br />
<br />
Форма также может определяться путем смены текстуры даже тогда, когда яркость и цвет остаются неизменными. Однако, наиболее типичным способом определения формы является комбинирование всех этих трех факторов. Примером такой комбинации могут служить рисунки Макса Эрнста (Max Ernst).<br />
<br />
&nbsp;</div>
